package request;

public class LogRequest {
	
	private String conteudo;
	private long vezes;
	
	public String getConteudo() {
		return conteudo;
	}
	public void setConteudo(String conteudo) {
		this.conteudo = conteudo;
	}
	public long getVezes() {
		return vezes;
	}
	public void setVezes(long vezes) {
		this.vezes = vezes;
	}
	

}
